public class Plus {
    //댓글
    String write;

    //게시물 번호
    int id;
    public Plus(String write, int id){
        this.write = write;
        this.id = id;
    }

    public String getWrite() {
        return write;
    }

    public void setWrite(String write) {
        this.write = write;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



}
